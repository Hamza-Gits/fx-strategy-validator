# MAIN
# ─────────────────────────────────────────────────────────────────────────────

def find_latest_html():
    candidates = glob.glob(os.path.join(BACKTEST5, '*.html'))
    if not candidates:
        # Walk up and search broadly
        for root, dirs, files in os.walk(os.path.join(REPO_ROOT, '..', '..', '..', '..')):
            for f in files:
                if 'ReportTester' in f and f.endswith('.html'):
                    candidates.append(os.path.join(root, f))
    if not candidates:
        return None
    return sorted(candidates, key=os.path.getmtime)[-1]


def main():
    html_path = sys.argv[1] if len(sys.argv) > 1 else find_latest_html()

    print(f'\n  MT5 report: {html_path}')
    if not html_path or not os.path.exists(html_path):
        print('\n  ERROR: MT5 HTML report not found.')
        print('  Run the NDCB_XAU_v1_2 backtest in MT5, save the report, then re-run.')
        print('  Or pass the path explicitly:')
        print('    python validation_harness/parity_oracle.py path/to/ReportTester.html')
        sys.exit(1)

    # ── Parse MT5 trades ──────────────────────────────────────────────────
    print('  Parsing MT5 HTML report...')
    mt5_df = parse_mt5_html(html_path)
    print(f'  Parsed {len(mt5_df)} MT5 trades')

    if mt5_df.empty:
        print('\n  WARNING: No trades extracted from MT5 report.')
        print('  The HTML parser may need tuning for this MT5 build version.')
        mt5_start, mt5_end = '2017-01-01', '2025-01-31'
    else:
        mt5_start = mt5_df['entry_time'].min().strftime('%Y-%m-%d')
        mt5_end   = mt5_df['entry_time'].max().strftime('%Y-%m-%d')
        print(f'  MT5 period: {mt5_start} → {mt5_end}')

    # ── Run Python trades on same period ──────────────────────────────────
    print(f'  Running Python ndcb_xau() on {mt5_start} → {mt5_end}...')
    py_df = run_python_trades(mt5_start, mt5_end)
    print(f'  Python fired {len(py_df)} trades')

    # ── Diff ──────────────────────────────────────────────────────────────
    diff = diff_trades(py_df, mt5_df)

    # ── Report ────────────────────────────────────────────────────────────
    report(diff, py_df, mt5_df)

    # ── Save to CSV ───────────────────────────────────────────────────────
    out_dir = os.path.join(REPO_ROOT, 'diagnostic')
    os.makedirs(out_dir, exist_ok=True)

    py_df.to_csv(os.path.join(out_dir, 'parity_python_trades.csv'), index=False)
    if not mt5_df.empty:
        mt5_df.to_csv(os.path.join(out_dir, 'parity_mt5_trades.csv'), index=False)

    if diff['matched']:
        pd.DataFrame(diff['matched']).to_csv(
            os.path.join(out_dir, 'parity_matched.csv'), index=False)

    print(f'  Trade lists saved to: {out_dir}/')


if __name__ == '__main__':
    main()
